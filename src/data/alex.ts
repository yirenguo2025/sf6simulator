// Helper to create a move entry
function m(
  id: string,
  input: string,
  category: MoveData['category'],
  data: Partial<MoveData>
): MoveData {
  return {
    id, input, category,
    name: '', damage: 0, startup: 0, active: '', recovery: 0, total: 0,
    hitAdv: '', blockAdv: '', guard: '', cancel: '-', hitconfirm: '',
    hitstun: 0, blockstun: 0, hitstop: 0,
    dmgScaling: '', drCancelOnHit: '', drCancelOnBlock: '',
    afterDrOnHit: '', afterDrOnBlock: '',
    punishAdv: '', perfParryAdv: '',
    driveDmgBlock: 0, driveDmgHit: 0, driveGain: 0,
    superGainHit: '', superGainBlock: '',
    juggleStart: '', juggleIncrease: '', juggleLimit: '',
    invuln: '', armor: '', airborne: '', range: '', notes: '',
    ...data,
  };
}

